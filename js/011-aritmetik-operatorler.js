//! ARİTMETİK OPERATÖRLER

//? "+" Toplama Operatörü

//todo Örnek:
let topla1 = 10;
let topla2 = 15;
console.log("Toplama: " + (topla1 + topla2));

//? "-" Çıkarma Operatörü

//todo Örnek:
let cikar1 = 25;
let cikar2 = 10;
console.log("Çıkarma: " + (cikar1 - cikar2));

//? "*" Çarpma Operatörü

//todo Örnek:
let carp1 = 5;
let carp2 = 8;
console.log("Çarpma: " + (carp1 * carp2));

//? "/" Bölme Operatörü

//todo Örnek:
let bolme1 = 40;
let bolme2 = 5;
console.log("Bölme: " + (bolme1/bolme2)/2);

//? "%" Mod Alma Operatörü

//todo Örnek:
console.log("Mod Alma: " + 12%3); // mod alma: 12'nin 3'e bölümünden kalan kaçtır?


//? "++" Artı Artı Operatörü : tanımlanan değeri 1 artırmak içindir.

//todo Örnek:
let artiArti = 10;

artiArti++;
artiArti++;
console.log("Artı artı operatörü: " + artiArti);

//? "--" Eksi Eksi Operatörü: tanımlanan değeri 1 artırmak içidnir.

//todo Örnek:
let eksiEksi = 10;

eksiEksi--;
eksiEksi--;
console.log("Eksi Eksi operatörü: " + eksiEksi);


//? "**" Üs Operatörü

console.log("Üs Operatörü: " + (5**3));


